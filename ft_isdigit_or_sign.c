int	ft_isdigit(int c)
{
	if (c < '0' || c > '9')
	{
		return (0);
	}
	return (1);
}

int	ft_isdigit_or_sign(int c)
{
	if (c < '+' || (c > '+' && c < '-') || (c > '-' && c < '0') || c > '9')
	{
		return (0);
	}
	return (1);
}
